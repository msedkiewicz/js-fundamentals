// Conditional statements
// if...else

if (condition) {
  statement;
} else if (!condition) {
  anotherStatement;
} else {
  somethingGeneric;
}

// switch... case

switch (variable) {
  case "one":
    somethingHappens;
    break;
  case "two":
    anotherSomethingHappens;
    break;
  default:
    somethingGeneric;
}

